package spring.services.impl;

import spring.entities.Account;
import spring.entities.Blog;
import spring.repositories.AccountRepo;
import spring.repositories.BlogRepo;
import spring.services.AccountService;
import spring.services.exceptions.AccountDoesNotExistException;
import spring.services.exceptions.AccountExistsException;
import spring.services.exceptions.BlogExistsException;
import spring.services.util.AccountList;
import spring.services.util.BlogList;

public class AccountServiceImpl implements AccountService {

    private AccountRepo accountRepo;

    private BlogRepo blogRepo;

    @Override
    public Account findAccount(Long id) {

        return accountRepo.findAccount(id);
    }

    @Override
    public Account createAccount(Account data) {
        Account account = accountRepo.findAccountByName(data.getName());
        if(account != null)
        {
            throw new AccountExistsException();
        }
        return accountRepo.createAccount(data);
    }

    @Override
    public Blog createBlog(Long accountId, Blog data) {
        Blog blogSameTitle = blogRepo.findBlogByTitle(data.getTitle());

        if(blogSameTitle != null)
        {
            throw new BlogExistsException();
        }

        Account account = accountRepo.findAccount(accountId);
        if(account == null)
        {
            throw new AccountDoesNotExistException();
        }

        Blog createdBlog = blogRepo.createBlog(data);

        createdBlog.setOwner(account);

        return createdBlog;
    }

    @Override
    public BlogList findBlogsByAccount(Long accountId) {
        Account account = accountRepo.findAccount(accountId);
        if(account == null)
        {
            throw new AccountDoesNotExistException();
        }
        return new BlogList(blogRepo.findBlogsByAccount(accountId));
    }

    @Override
    public AccountList findAllAccounts() {

        return new AccountList(accountRepo.findAllAccounts());
    }

    @Override
    public Account findByAccountName(String name) {
        return accountRepo.findAccountByName(name);
    }
}
